    private void btnVoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVoltarActionPerformed
        // TODO add your handling code here:
        this.dispose();
        Loading load = new Loading();
        load.setVisible(true);
        ListaPets lista = new ListaPets();
        
        ScheduledExecutorService scheduler = Executors.newScheduledThreadPool(1);

        scheduler.schedule(() -> {
            load.dispose();
            lista.setVisible(true);
        }, 1, TimeUnit.SECONDS);
        // Fecha o agendador ap√≥s a execu√ß√£o da tarefa
        scheduler.shutdown();
    }//GEN-LAST:event_btnVoltarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String nome = inputNome.getText();
        String raca = inputRaca.getText();
        String cor = inputCor.getText();
        String tipo = (String) inputTipo.getSelectedItem();
        if(nome.length() == 0 || raca.length() == 0 || cor.length() == 0 || tipo.equals("Selecione")){
            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos");
        }else{
            int sucesso = new PetController().create(nome, raca, cor, tipo);
            if(sucesso == 0){
                JOptionPane.showMessageDialog(null, "Parece que n√£o foi poss√≠vel cadastrar seu pet");
            }else{
                JOptionPane.showMessageDialog(null, "Pet cadastrado com sucesso");
                inputNome.setText("");
                inputRaca.setText("");
                inputCor.setText("");
                inputTipo.setSelectedIndex(0);
            }
            
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

